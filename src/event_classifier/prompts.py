"""이벤트 분류 파이프라인용 LLM 프롬프트 빌더"""

def build_extract_main_event_prompt(article_text: str) -> str:
    """
    기사 내 가장 중추적인 대표 사건(Main Event)을 추출하는 프롬프트.
    
    기사의 대표 사건이 비교 이벤트에 따라 흔들리는 상황을 방지하고자 기사 단독으로 대표 사건을 먼저 추출한다.
    """

    return f"""
기사에서 가장 중요한 이벤트(main event)를 하나 추출하세요.

이벤트란:
- 특정 시점에 새롭게 발생하거나 확인된 행위, 결정, 발표, 결과 또는 상태 변화

규칙:
- 기사의 신규 정보를 우선 선택
- 제목과 초반부를 우선 참고
- 기사의 핵심 행위만 남기고 부가 설명은 제거할 것
- 날짜가 명시되어 있을 시 반드시 포함할 것. 날짜 표현은 기사 원문 그대로 사용할 것
- 시제와 상태를 변경하지 말 것

출력 규칙:
- JSON만 출력
- 설명 금지
- markdown 금지

예시:
{{
  "main_event": "정부와 의료계 재협상 결렬"
}}

기사:
{article_text}
"""

def build_verify_event_prompt(main_event: str, event_core_content: str) -> str:
    """추출된 대표 사건과 기존 이벤트 코어가 실질적으로 같은 사안인지 정밀 교차 검증하는 프롬프트"""

    return f"""
대표 사건과 이벤트가 동일한 사건인지 판단하세요.

규칙:
- 같은 주제가 아니라 동일 사건인지 판단
- 행위, 상태, 절차가 다르면 낮은 점수
  (예: 예정, 진행, 종료, 발표, 결렬 등)
- 핵심 엔티티(기관, 장소, 인물 등)가 불일치하면 낮은 점수
- reason은 입력에 있는 정보만 사용
- 상대 날짜(오늘, 어제 등)를 실제 날짜로 변환하지 말 것
- 날짜가 한쪽에만 존재하는 경우 날짜 일치를 근거로 사용하지 말 것(상대 날짜는 날짜가 존재하는 않는 것으로 판단)

점수 기준:
- 0.90~1.00 : 동일 사건
- 0.70~0.89 : 매우 유사
- 0.40~0.69 : 같은 주제
- 0.00~0.39 : 무관

출력 규칙:
- JSON만 출력
- reason은 30자 이내
- markdown 금지

예시:
{{
  "score": 0.93,
  "reason": "동일 학교 급식 이물질 사건"
}}


신규 기사 대표 사건:
{main_event}

기존 이벤트 핵심 내용:
{event_core_content}
"""

def build_generate_event_title_prompt(trimmed_content: str) -> str:
    """핵심 사건 정보를 바탕으로 뉴스 서비스에 적합한 깔끔하고 명확한 이벤트 제목을 생성합니다."""
    return f"""
당신은 뉴스 서비스의 에디터입니다. 아래 제공된 '사건 정보'를 바탕으로, 해당 사건을 대표할 수 있는 명확하고 간결한 뉴스 이벤트 제목을 1줄로 생성하세요.

[지침]
1. 따옴표나 불필요한 수식어는 제외하고 제목만 담백하게 출력하세요.
2. 20자 내외로 직관적이게 작성하세요.
3. 출력 형식은 반드시 아래 JSON 포맷을 정확히 지켜야 합니다.

[사건 정보]
{trimmed_content}

[출력 포맷]
{{"event_title": "생성된 이벤트 제목"}}
"""